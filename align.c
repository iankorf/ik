static int Matrix = -1;
static int MAT[25][25];
static int GAP;

void set_matrix (int N) {
	int i, j;
	
	if (Matrix == N) return;
	Matrix = N;
	
	if (N == 40) {
		for (i = 0; i < 25; i++) for (j = 0; j < 25; j++) MAT[i][j] = B40[i][j];
		GAP = -4;
	} else if (N == 62) {
		for (i = 0; i < 25; i++) for (j = 0; j < 25; j++) MAT[i][j] = B62[i][j];
		GAP = -5;
	} else if (N == 80) {
		for (i = 0; i < 25; i++) for (j = 0; j < 25; j++) MAT[i][j] = B80[i][j];
		GAP = -7;
	} else if (N == 0) {
		for (i = 0; i < 25; i++) for (j = 0; j < 25; j++) MAT[i][j] = NT[i][j];
		GAP = -2;
	} else {
		ik_exit(1, "invalid matrix identifier");
	}
	
}

double sw_mmg (const char *s1, const char *s2, int m, int n, int g) {
	int i, j, l1, l2, max_i, max_j, min_i, min_j, pos;
	double d, v, h, max_s = 0;
	double ** sm;
	char ** tm;
	char *a1, *a2, *a3;
	
	l1 = strlen(s1), l2 = strlen(s2);
	
	/* allocate matrices */
	sm = malloc(sizeof(double*) * (l1+1));
	tm = malloc(sizeof(double*) * (l1+1));
	for (i = 0; i <= l1; i++)  sm[i] = malloc(sizeof(double) * (l2+1));
	for (i = 0; i <= l1; i++)  tm[i] = malloc(sizeof(double) * (l2+1));
		
	/* init first column and row */
	for (i = 0; i <= l1; i++) sm[i][0] = 0, tm[i][0] = '.';
	for (j = 1; j <= l2; j++) sm[0][j] = 0, tm[0][j] = '.';
	
	/* fill matrix */
	for (i = 1; i <= l1; i++) {
		for (j = 1; j <= l2; j++) {
			if (s1[i-1] == s2[j-1]) d = sm[i-1][j-1] + m;
			else                    d = sm[i-1][j-1] + n;
			h = sm[i-1][j] + g;
			v = sm[i][j-1] + g;
			
			if (d > h && d > v && d > 0) sm[i][j] = d, tm[i][j] = 'd';
			else if (h > v && h > 0)     sm[i][j] = h, tm[i][j] = 'h';
			else if (v > 0)              sm[i][j] = v, tm[i][j] = 'v';
			else                         sm[i][j] = 0, tm[i][j] = '.';
			
			if (d > max_s) max_s = d, max_i = i, max_j = j;
		}
	}
		
	/* allocate alignment strings */
	a1 = malloc((l1 + l2 + 1) * sizeof(char));
	a2 = malloc((l1 + l2 + 1) * sizeof(char));
	a3 = malloc((l1 + l2 + 1) * sizeof(char));
	
	/* traceback */
	i = max_i, j = max_j, pos = 0;
	while (sm[i][j] > 0) {
		a1[pos] = s1[i-1];
		a2[pos] = s2[j-1];
		min_i = i;
		min_j = j;
		
		pos++;
		if      (tm[i][j] == 'd') i--, j--;
		else if (tm[i][j] == 'h') j--;
		else if (tm[i][j] == 'v') i--;
	}
	a1[pos] = '\0', a2[pos] = '\0', a3[pos] = '\0';
		
	/* reverse strings and make alignment string */
	for (i = 0; i < strlen(a1); i++) a3[pos -i -1] = a1[i];
	for (i = 0; i < strlen(a2); i++) a1[pos -i -1] = a2[i];
	
	/* clean up */
	free(a1); free(a2); free(a3);
	
	for (i = 0; i <= l1; i++) {
		free(sm[i]);
		free(tm[i]);
	}
	free(sm);
	free(tm);
	
	return max_s;
}

double sw_mmg_linear (const char *s1, const char *s2, int m, int n, int g) {
	int i, j, k, l1, l2, max_i, max_j;
	double d, v, h, max_s = 0;
	double *r1, *r2;
		
	/* init */
	l1 = strlen(s1);
	l2 = strlen(s2);
	r1 = malloc(sizeof(double) * l2 + 1);
	r2 = malloc(sizeof(double) * l2 + 1);
	for (j = 0; j <= l2; j++) r1[j] = 0;
	r2[0] = 0;
	
	/* fill */
	for (i = 1; i <= l1; i++) {
		for (j = 1; j <= l2; j++) {
			if (s1[i-1] == s2[j-1]) d = r1[j-1] + m;
			else                    d = r1[j-1] + n;
			h = r1[j] + g;
			v = r2[j-1] + g;
						
			if      (d > h && d > v && d > 0) r2[j] = d;
			else if (h > v && h > 0)          r2[j] = h;
			else if (v > 0)                   r2[j] = v;
			else                              r2[j] = 0;
			
			if (d > max_s) max_s = d, max_i = i, max_j = j;
		}
		
		/* copy row 2 to row 1*/
		for (k = 0; k <= l2; k++) r1[k] = r2[k];
		r2[0] = 0;
	}
			
	/* clean up */
	free(r1);
	free(r2);
	
	return max_s;
}

double sw_mat (const char *s1, const char *s2, int blosum) {
	int i, j, l1, l2, max_i, max_j, min_i, min_j, pos;
	double d, v, h, max_s = 0;
	double ** sm;
	char ** tm;
	char *a1, *a2, *a3;
	int a, b;
	
	l1 = strlen(s1), l2 = strlen(s2);
	set_matrix(blosum);
	
	/* allocate matrices */
	sm = malloc(sizeof(double*) * (l1+1));
	tm = malloc(sizeof(double*) * (l1+1));
	for (i = 0; i <= l1; i++)  sm[i] = malloc(sizeof(double) * (l2+1));
	for (i = 0; i <= l1; i++)  tm[i] = malloc(sizeof(double) * (l2+1));
		
	/* init first column and row */
	for (i = 0; i <= l1; i++) sm[i][0] = 0, tm[i][0] = '.';
	for (j = 1; j <= l2; j++) sm[0][j] = 0, tm[0][j] = '.';
	
	/* fill matrix */
	for (i = 1; i <= l1; i++) {
		for (j = 1; j <= l2; j++) {
			a = s1[i-1] - 'A';
			b = s2[j-1] - 'A';
			d = sm[i-1][j-1] + MAT[a][b];
			h = sm[i-1][j  ] + GAP;
			v = sm[i  ][j-1] + GAP;
			
			if (d > h && d > v && d > 0) sm[i][j] = d, tm[i][j] = 'd';
			else if (h > v && h > 0)     sm[i][j] = h, tm[i][j] = 'h';
			else if (v > 0)              sm[i][j] = v, tm[i][j] = 'v';
			else                         sm[i][j] = 0, tm[i][j] = '.';
			
			if (d > max_s) max_s = d, max_i = i, max_j = j;
		}
	}
		
	/* allocate alignment strings */
	a1 = malloc((l1 + l2 + 1) * sizeof(char));
	a2 = malloc((l1 + l2 + 1) * sizeof(char));
	a3 = malloc((l1 + l2 + 1) * sizeof(char));
	
	/* traceback */
	i = max_i, j = max_j, pos = 0;
	while (sm[i][j] > 0) {
		a1[pos] = s1[i-1];
		a2[pos] = s2[j-1];
		min_i = i;
		min_j = j;
		
		pos++;
		if      (tm[i][j] == 'd') i--, j--;
		else if (tm[i][j] == 'h') j--;
		else if (tm[i][j] == 'v') i--;
	}
	a1[pos] = '\0', a2[pos] = '\0', a3[pos] = '\0';
		
	/* reverse strings and make alignment string */
	for (i = 0; i < strlen(a1); i++) a3[pos -i -1] = a1[i];
	for (i = 0; i < strlen(a2); i++) a1[pos -i -1] = a2[i];
	
	/* clean up */
	free(a1); free(a2); free(a3);
	
	for (i = 0; i <= l1; i++) {
		free(sm[i]);
		free(tm[i]);
	}
	free(sm);
	free(tm);
	
	return max_s;
}

double sw_mat_linear (const char *s1, const char *s2, int blosum) {
	int i, j, k, l1, l2, max_i, max_j, a, b;
	double d, v, h, max_s = 0;
	double *r1, *r2;
		
	/* init */
	l1 = strlen(s1);
	l2 = strlen(s2);
	set_matrix(blosum);
	r1 = malloc(sizeof(double) * l2 + 1);
	r2 = malloc(sizeof(double) * l2 + 1);
	for (j = 0; j <= l2; j++) r1[j] = 0;
	r2[0] = 0;

	/* fill */
	for (i = 1; i <= l1; i++) {
		for (j = 1; j <= l2; j++) {
			a = s1[i-1] - 'A';
			b = s2[j-1] - 'A';
			d = r1[j-1] + MAT[a][b];
			h = r1[j] + GAP;
			v = r2[j-1] + GAP;
						
			if      (d > h && d > v && d > 0) r2[j] = d;
			else if (h > v && h > 0)          r2[j] = h;
			else if (v > 0)                   r2[j] = v;
			else                              r2[j] = 0;
			
			if (d > max_s) max_s = d, max_i = i, max_j = j;
		}
		
		/* copy row 2 to row 1*/
		for (k = 0; k <= l2; k++) r1[k] = r2[k];
		r2[0] = 0;
	}
			
	/* clean up */
	free(r1);
	free(r2);
	
	return max_s;
}


